class QRStatsBot:
    """Encapsulated Telegram bot logic."""

    TOKEN = settings.TELEGRAM_BOT_TOKEN

    def __init__(self) -> None:
        self.app = ApplicationBuilder().token(self.TOKEN).build()
        self._register_handlers()

    # ‚îÄ‚îÄ‚îÄ Handler registration ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

    def _register_handlers(self) -> None:
        self.app.add_handler(CommandHandler("start", self.cmd_start))
        self.app.add_handler(CommandHandler("help", self.cmd_help))
        self.app.add_handler(CommandHandler("stats", self.cmd_stats))
        self.app.add_handler(CommandHandler("allstats", self.cmd_allstats))
        self.app.add_handler(CommandHandler("compare", self.cmd_compare))
        self.app.add_handler(CommandHandler("dashboard", self.cmd_dashboard))
        self.app.add_handler(CallbackQueryHandler(self.cb_handler))
        self.app.add_error_handler(self.error_handler)

    # ‚îÄ‚îÄ‚îÄ Command handlers ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

    async def cmd_start(self, update: Update, context: ContextTypes.DEFAULT_TYPE):
        user = update.effective_user
        is_admin = user and user.username in ADMIN_USERNAMES
        msg = (
            "üëã –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å!\n\n"
            "/stats [d] ‚Äî —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ (d –¥–Ω–µ–π, 30 –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é)\n"
            "/help ‚Äî —Å–ø–∏—Å–æ–∫ –∫–æ–º–∞–Ω–¥"
        )
        if is_admin:
            msg += (
                "\n\nüîê *Admin*\n"
                "/allstats [d] ‚Äî –ø–æ –≤—Å–µ–º –ª–æ–∫–∞—Ü–∏—è–º\n"
                "/compare ‚Äî —Å—Ä–∞–≤–Ω–∏—Ç—å –ª–æ–∫–∞—Ü–∏–∏\n"
                "/dashboard ‚Äî –∏–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω–∞—è –ø–∞–Ω–µ–ª—å"
            )
        await update.message.reply_text(msg, parse_mode="Markdown")

    async def cmd_help(self, update: Update, context: ContextTypes.DEFAULT_TYPE):
        await self.cmd_start(update, context)

    async def cmd_stats(self, update: Update, context: ContextTypes.DEFAULT_TYPE):
        days = (
            int(context.args[0]) if context.args and context.args[0].isdigit() else 30
        )
        await self._send_stats(update, days, admin_scope=False)

    @admin_only
    async def cmd_allstats(self, update: Update, context: ContextTypes.DEFAULT_TYPE):
        days = (
            int(context.args[0]) if context.args and context.args[0].isdigit() else 30
        )
        await self._send_stats(update, days, admin_scope=True)

    @admin_only
    async def cmd_compare(self, update: Update, context: ContextTypes.DEFAULT_TYPE):
        await self._send_compare(update)

    @admin_only
    async def cmd_dashboard(self, update: Update, context: ContextTypes.DEFAULT_TYPE):
        kb = self._dashboard_keyboard()
        await update.message.reply_text(
            "üìä *–ü–∞–Ω–µ–ª—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏* ‚Äî –≤—ã–±–µ—Ä–∏—Ç–µ –¥–∏–∞–ø–∞–∑–æ–Ω:",
            reply_markup=InlineKeyboardMarkup(kb),
            parse_mode="Markdown",
        )

    # ‚îÄ‚îÄ‚îÄ Callback queries ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

    async def cb_handler(self, update: Update, context: ContextTypes.DEFAULT_TYPE):
        query = update.callback_query
        if not query or not query.data:
            return
        cmd, *arg = query.data.split(":")
        if cmd == "range":
            days = Range.to_days(arg[0])
            await self._send_stats(update, days, admin_scope=True, edit=True)
        elif cmd == "compare":
            await self._send_compare(update, edit=True)
        elif cmd == "back":
            await self.cmd_dashboard(update, context)
        await query.answer()

    # ‚îÄ‚îÄ‚îÄ Internal helpers ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

    async def _send_stats(
        self,
        update: Update,
        days: int,
        *,
        admin_scope: bool,
        edit: bool = False,
    ) -> None:
        start = timezone.now().date() - _dt.timedelta(days=days)
        total = QRCodeScan.objects.filter(timestamp__date__gte=start).count()
        lines: list[str] = [
            f"üìä *–°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –∑–∞ {days} –¥–Ω–µ–π*",
            f"–í—Å–µ–≥–æ —Å–∫–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–π: {total}",
        ]
        if admin_scope:
            for loc in Location.objects.all():
                cnt = loc.scans.filter(timestamp__date__gte=start).count()
                pct = (cnt / total * 100) if total else 0
                lines.append(f"*{loc.name}*: {cnt}  ({pct:.1f}% )")
        await self._reply(update, "\n".join(lines), edit=edit)

    async def _send_compare(self, update: Update, *, edit: bool = False):
        today = timezone.now().date()
        locs = Location.objects.all()
        sections: list[str] = ["üìä *–°—Ä–∞–≤–Ω–µ–Ω–∏–µ –ª–æ–∫–∞—Ü–∏–π*\n"]
        for label, delta in (
            ("–°–µ–≥–æ–¥–Ω—è", 0),
            ("–í—á–µ—Ä–∞", 1),
            ("7 –¥–Ω–µ–π", 7),
            ("30 –¥–Ω–µ–π", 30),
        ):
            start = today - _dt.timedelta(days=delta)
            sections.append(f"*{label}*:")
            for loc in locs:
                cnt = (
                    loc.scans.filter(timestamp__date=today).count()
                    if delta == 0
                    else loc.scans.filter(timestamp__date__gte=start).count()
                )
                sections.append(f"{loc.name}: {cnt}")
            sections.append("")
        await self._reply(update, "\n".join(sections), edit=edit)

    def _dashboard_keyboard(self) -> list[list[InlineKeyboardButton]]:
        btns = [
            ("–°–µ–≥–æ–¥–Ω—è", Range.TODAY),
            ("–í—á–µ—Ä–∞", Range.YESTERDAY),
            ("7 –¥–Ω–µ–π", Range.WEEK),
            ("30 –¥–Ω–µ–π", Range.MONTH),
            ("–í—Å–µ –≤—Ä–µ–º—è", Range.ALL),
        ]
        rows = [
            [InlineKeyboardButton(t, callback_data=f"range:{r.value}")] for t, r in btns
        ]
        rows.append(
            [InlineKeyboardButton("–°—Ä–∞–≤–Ω–∏—Ç—å –ª–æ–∫–∞—Ü–∏–∏", callback_data="compare:")]
        )
        return rows

    async def _reply(self, update: Update, text: str, *, edit: bool = False):
        if edit and update.callback_query:
            await update.callback_query.edit_message_text(text, parse_mode="Markdown")
        else:
            await update.effective_message.reply_text(text, parse_mode="Markdown")

    # ‚îÄ‚îÄ‚îÄ Global error handler ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

    async def error_handler(self, update: object, context: ContextTypes.DEFAULT_TYPE):  # noqa: D401,E501
        logger.error("Exception caught:")
        logger.error(context.error)
        logger.error("\n" + traceback.format_exc())
        if isinstance(update, Update) and update.effective_message:
            await update.effective_message.reply_text(
                "–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞. –ê–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä —É–≤–µ–¥–æ–º–ª–µ–Ω."
            )

    # ‚îÄ‚îÄ‚îÄ Entrypoint ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

    def run(self) -> None:
        logger.info("QRStats bot starting‚Ä¶")
        self.app.run_polling()


