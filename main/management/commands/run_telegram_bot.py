    """Block access to a handler for non‚Äëadmin users."""

    @wraps(func)
    async def _wrapped(
        self: "QRStatsBot", update: Update, context: ContextTypes.DEFAULT_TYPE
    ):  # noqa: D401,E501
        user = update.effective_user
        if user is None or user.username not in ADMIN_USERNAMES:
            await update.effective_message.reply_text("‚õî –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –ø—Ä–∞–≤.")
            return
        return await func(self, update, context)

    return _wrapped


