class Range(str, Enum):
    TODAY = "today"
    YESTERDAY = "yesterday"
    WEEK = "7d"
    MONTH = "30d"
    ALL = "all"

    @classmethod
    def to_days(cls, label: str) -> int:
        mapping = {
            cls.TODAY.value: 0,
            cls.YESTERDAY.value: 1,
            cls.WEEK.value: 7,
            cls.MONTH.value: 30,
            cls.ALL.value: 10 * 365,  # proxy for ‚Äúall time‚Äù
        }
        return mapping.get(label, 30)


